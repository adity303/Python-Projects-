#Wap to convert decimal to binary, octal and hexadecimal. 
decimal = int(input("Enter the number here: "))

print("The conversion of decimal number", decimal, "is: ")
# Python number conversion 
print(bin(decimal), "in binary")  # decimal to binary
print(oct(decimal), "in octal") # decimal to octal
print(hex(decimal), "in hexadecimal") # decimal to hexadecimal 